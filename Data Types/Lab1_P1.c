#include <stdio.h>

int main() {
    int Integer = 10;
    float Float = 3.14;
    char Character = 'A';
    double Double = 9.8765;

    printf("Integer: %d\n",Integer);
    printf("Float:%.2f\n", Float);
    printf("Character: %c\n",Character);
    printf("Double: %.4f\n", Double);
    return 0;
}
